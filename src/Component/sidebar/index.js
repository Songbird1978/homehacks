export default SideBar ; 

function CustomLink({ to, children, ...props }) {
    const path = window.location.pathname

    return (
        <div>
       <li className={path === to  ? "active" : ""}>
       <Link to={to} {...props}>
        {children}
       </Link>
       </li>
       </div>
    )
} 
    